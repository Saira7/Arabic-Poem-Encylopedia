package transferObject;

public class VerseTO {
    private int verseId;
    private int noOfVerses;
    private int poemId;

    public int getVerseId() {
        return verseId;
    }

    public void setVerseId(int verseId) {
        this.verseId = verseId;
    }

    public int getNoOfVerses() {
        return noOfVerses;
    }

    public void setNoOfVerses(int noOfVerses) {
        this.noOfVerses = noOfVerses;
    }

    public int getPoemId() {
        return poemId;
    }

    public void setPoemId(int poemId) {
        this.poemId = poemId;
    }
}
